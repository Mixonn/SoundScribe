<script>
import { Scatter, mixins } from 'vue-chartjs'

export default {
  extends: Scatter,
  mixins: mixins.reactiveProp,
  props: {
    chartData: {
      type: Object,
      default: null
    },
    options: {
      type: Object,
      default: null
    }
  },
  watch: {
    chartData () {
      this.renderChart(this.chartData, this.options)
    }
  },
  mounted () {
    this.renderChart(this.chartData, this.options)
  }
}
</script>
